public class ContractInformationReader extends BlockInfoReader
{

    @Override
    public void parseResponsePart(Document xmlRespDoc, Map<String, List<DocumentumFile>> existingFilesByType,
            Object objToPopulate)
    {
        if (!(objToPopulate instanceof ContractInformation))
        {
            throw new IllegalArgumentException("objToPopulate to ContractInfoReader should be a Contract block!");
        }
        if (xmlRespDoc != null)
        {
            ContractInformation infoObj = (ContractInformation) objToPopulate;

            Element propertiesElem = (Element) xmlRespDoc.selectSingleNode("//objects/object/properties");
            DocumentumAnnotationProcessor.beanDataFromXml(infoObj, propertiesElem, existingFilesByType);
        }
    }
}
